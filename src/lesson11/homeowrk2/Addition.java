public class Addition {
    //整数和整数
    public int addition(int m, int n) {
        return m + n;
    }

    //整数和浮点
    public float addition(int m, float n) {
        return m + n;
    }

    // 整数和字符串
    public String addition(int m, String n) {
        return m + n;
    }

    //浮点和浮点
    public float addition(float m, float n) {
        return m + n;
    }

    //浮点和字符串
    public String addition(float m, String n) {
        return m + n;
    }

    // 双精度浮点和整数
    public double addition(double m, int n) {
        return m + n;
    }

    //双精度浮点和双精度浮点
    public double addition(double m, double n) {
        return m + n;
    }

    // 双精度浮点和字符串
    public String addition(double m, String n) {
        return m + n;
    }
}